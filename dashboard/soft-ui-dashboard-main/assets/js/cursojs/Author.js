class Author{
    constructor(foto, nombre, correo, puesto, departamento, fechaIngreso, estatus){
        this.foto = foto;
        this.nombre = nombre;
        this.correo = correo;
        this.puesto = puesto;
        this.departamento = departamento;
        this.fechaIngreso = fechaIngreso;
        this.estatus = estatus;
    }
}